const Joi = require("joi");
const propertySchema = Joi.object({
  Address: Joi.string().required(),
  PropertyName: Joi.string().required(),
  Description: Joi.string().required(),
  Category: Joi.string().required(),
  SubCategory: Joi.string().required(),
  PropertyType: Joi.string().required(),
  PropertySubType: Joi.string().required(),
  PropertyTypeRadio: Joi.string().required(),
  beds: Joi.number().min(0).required(),
  baths: Joi.number().min(0).required(),
  reception: Joi.number().min(0).required(),
  tenure: Joi.string().required(),
  size: Joi.string().required(),
  price: Joi.string().required(),
  priceType: Joi.string().required(),
  minimumAcceptablePrice: Joi.number().required(),
  selectType: Joi.string().required(),
  referalFee: Joi.string().required(),
  image: Joi.array().items(Joi.string()).required(),
  video: Joi.array().items(Joi.string()).required(),
  virtualTourUrl: Joi.string().required(),
  energyEfficiencyRating: Joi.array().items(Joi.string()).required(),
  floorPlan: Joi.array().items(Joi.string()).required(),
  brochure: Joi.array().items(Joi.string()).required(),
  energyEfficiencyRatingType: Joi.string().required(),
  brochureType: Joi.string().required(),
  floorPlanType: Joi.string().required(),
  miscellaneousType: Joi.string().required(),
  miscellaneous: Joi.array().items(Joi.string()).required(),
  keyFeature: Joi.array().items(Joi.string()).required(),
  tags: Joi.array().items(Joi.string()).required(),
  planType: Joi.string().required(),
  authId: Joi.string().required(),
  lat: Joi.number().required(),
  lng: Joi.number().required(),
});
const requirementsSchema = Joi.object({
  category: Joi.string().required(),
  location: Joi.string().allow("").optional(),
  lat: Joi.number().required(),
  lng: Joi.number().required(),
  radius: Joi.string().allow("").optional(),
  propertyType: Joi.string().required(),
  propertySubType: Joi.string().required(),
  size: Joi.string().required(),
  addedToSite: Joi.string().allow("").optional(),
  soldType: Joi.string().allow("").optional(),
  minPriceRange: Joi.string().required(),
  maxPriceRange: Joi.string().required(),
  clientName: Joi.string().required(),
  clientEmail: Joi.string().required(),
  keyFeature: Joi.array().items(Joi.string()).required(),
  tags: Joi.array().items(Joi.string()).required(),
  Location: Joi.string().required(),
  Radius: Joi.string().required(),
  authId: Joi.string().required(),
  createdAt: Joi.string().required(),
});
const offersSchema = Joi.object({
  notes: Joi.string().allow("").optional(),
  price: Joi.number().required(),
  authId: Joi.string().required(),
  agentAuthId: Joi.string().required(),
  propertyId: Joi.string().required(),
});
const viewingsSchema = Joi.object({
  date: Joi.date().iso().required(),
  slot: Joi.string().required(),
  notes: Joi.string().allow("").optional(),
  authId: Joi.string().required(),
  agentAuthId: Joi.string().required(),
  propertyId: Joi.string().required(),
  status: Joi.string().required(),
});
const notificationSchema = Joi.object({
  authId: Joi.string().required(),
  message: Joi.string().required(),
  type: Joi.string().required(),
  createdAt: Joi.string().required(),
  isRead: Joi.boolean().required(),
  propertyId: Joi.string().required(),
  agentAuthId: Joi.string().required(),
});
module.exports = {
  propertySchema,
  requirementsSchema,
  offersSchema,
  viewingsSchema,
  notificationSchema,
};
